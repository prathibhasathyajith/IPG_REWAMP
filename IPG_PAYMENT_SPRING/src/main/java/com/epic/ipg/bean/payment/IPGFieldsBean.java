package com.epic.ipg.bean.payment;

/**
 *
 * @author prathibha_w
 */
public class IPGFieldsBean {
    private String firstname;
    private String cardNumber;
    private String cvcNum;
    private String cvvNum;
    private String cidNum;
    private String month;
    private String year;
    private String code;
    

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getCardNumber() {
        return cardNumber;
    }

    public void setCardNumber(String cardNumber) {
        this.cardNumber = cardNumber;
    }

    public String getCvcNum() {
        return cvcNum;
    }

    public void setCvcNum(String cvcNum) {
        this.cvcNum = cvcNum;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getCidNum() {
        return cidNum;
    }

    public void setCidNum(String cidNum) {
        this.cidNum = cidNum;
    }

    public String getCvvNum() {
        return cvvNum;
    }

    public void setCvvNum(String cvvNum) {
        this.cvvNum = cvvNum;
    }
    
    
    
}
